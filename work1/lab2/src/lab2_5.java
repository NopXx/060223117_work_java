public class lab2_5 {
    public static void main(String[] args) {
        System.out.println(true);
        // แสดง boolean true
        System.out.println(false);
        // แสดง boolean false
        System.out.println('A');
        // แสดงตัวอักษร A 1 ตัว
        System.out.println('\101');
        // แสดงตัวอักษร ASCII A
        System.out.println('\u0041');
        // แสดงตัวอักษร Unicode A
        System.out.println("Code\tName");
        // แสดง format string 1 tab
        System.out.println("Line1\r\nLine2");
        // ขึ้นบรรทัดใหม่
        System.out.println("Page 1\fPage 2");
        // แสดงสัญลักษณ์ ♀
    }
}
