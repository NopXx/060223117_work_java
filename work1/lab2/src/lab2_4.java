public class lab2_4 {
    public static void main(String[] args) {
        System.out.println(536871066);
        // แสดงผลเลขฐาน 10 ค่าสูงสุด
        System.out.println(12300000000L);
        // แสดงผลเลขฐาน 10 มากกว่าค่าสูงสุด

        // 8^2*1 + 8^1*2 + 8^0*3
        System.out.println(0123);
        // 16^2*1 + 16^1*2 + 16^0*3
        System.out.println(0x123);

        System.out.println(123.50);
        // แสดงตัวเลขทศนิยม
        System.out.println(123.5f);
        // แสดงตัวเลขทศนิยม
        System.out.println(123e3);
        // แสดงเลข 0 3 ตัว
        System.out.println(123e-2);
        // แสดงตัวเลข 1.23
    }
}
